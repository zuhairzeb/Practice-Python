# Create a list of items
shopping_list = ["milk", "bread", "eggs", "bananas"]

# Access, change, append, remove, and count items in the list
shopping_list[0] = "apple"  # Replace "milk" with "apple"
shopping_list.append("oranges")  # Add "oranges" to the end
shopping_list.remove("bread")  # Remove "bread"

print(shopping_list)  # Output: ['apple', 'eggs', 'bananas', 'oranges']
print("Total Items:", len(shopping_list))  # Output: Total Items: 4
